def load_questions():
    with open('assets/questions.json', 'r', encoding='utf-8') as f:
        return json.load(f)

def save_questions(questions):
    with open('assets/questions.json', 'w', encoding='utf-8') as f:
        json.dump(questions, f, ensure_ascii=False, indent=2)

def remove_image_tags(explanation):
    """解説文中の画像タグを削除し、適切なテキスト説明に置き換える"""
    # ![alt](url) 形式の画像タグを検出
    pattern = r'!\[([^\]]*)\]\(([^)]+)\)'
    
    # 画像タグを削除（説明文は残す）
    cleaned = re.sub(pattern, '', explanation)
    
    # 連続する空行を1つにまとめる
    cleaned = re.sub(r'\n\n\n+', '\n\n', cleaned)
    
    return cleaned


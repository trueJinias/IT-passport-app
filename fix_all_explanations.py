def fix_remaining_explanations():
    questions = load_questions()
    
    modified_count = 0
    
    for q in questions:
        qid = q.get('id')
        explanation = q.get('explanation', '')
        
        # 画像タグを検索
        pattern = r'!\[([^\]]*)\]\(([^)]+\.png[^)]*)\)'
        matches = re.findall(pattern, explanation)
        
        if matches:
            # 画像タグを削除
            explanation = remove_image_tags(explanation)
            
            # 追加テキストがあれば挿入
            if qid in additional_fixes:
                fix = additional_fixes[qid]
                if '【ポイント】' in explanation:
                    parts = explanation.split('【ポイント】')
                    explanation = parts[0].rstrip() + '\n' + fix["text"] + '\n\n【ポイント】' + parts[1]
                else:
                    explanation = explanation.rstrip() + '\n' + fix["text"]
            else:
                # 追加テキストがない場合は、画像削除のみ行う
                # 空行の整理
                pass
            
            q['explanation'] = explanation
            modified_count += 1
            print(f"Fixed ID {qid}: {q.get('url', '')}")
    
    save_questions(questions)
    print(f"\n合計 {modified_count} 件の問題を修正しました。")

if __name__ == '__main__':
    fix_remaining_explanations()
